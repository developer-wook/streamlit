# ============================================================
# Chapter 6. 레이아웃 — 그로스 대시보드 만들기
# 실행: streamlit run app6_layout.py
# (데이터 생성부는 Ch.5와 동일 — 이 챕터의 초점은 "화면 구조"입니다)
# ============================================================
import numpy as np
import pandas as pd
import streamlit as st

st.set_page_config(page_title="Ch6. 레이아웃", page_icon="📐", layout="wide")

# ----- 데이터 준비 (Ch.5 재사용, 설명 생략) -----
@st.cache_data
def make_data():
    rng = np.random.default_rng(42)
    dates = pd.date_range("2026-06-01", periods=30, freq="D")
    return pd.DataFrame({
        "날짜": dates,
        "방문자": (rng.normal(1000, 150, 30).cumsum() / 10 + 800).astype(int),
        "가입자": (rng.normal(80, 20, 30).cumsum() / 10 + 50).astype(int),
        "매출(만원)": (rng.normal(300, 60, 30).cumsum() / 10 + 200).astype(int),
    })

df = make_data()

# ============================================================
# [사이드바] 필터 영역
# with st.sidebar: 블록 안의 모든 출력은 왼쪽 사이드바에 그려집니다.
# [설계 원칙] "화면을 조작하는 도구"는 사이드바에,
#             "조작의 결과"는 본문에 — 역할을 분리하면 화면이 읽기 쉬워집니다.
# ============================================================
with st.sidebar:
    st.title("📐 필터")
    days = st.slider("최근 N일", 7, 30, 14)          # 몇 일치를 볼 것인가
    metrics = st.multiselect("표시할 지표",
                             ["방문자", "가입자", "매출(만원)"],
                             default=["방문자", "가입자"])   # 처음부터 선택된 항목
    st.divider()
    st.caption("필터를 바꾸면 → 전체 재실행 → 본문이 새 필터값으로 다시 그려집니다")

# 필터 적용: tail(n) = 마지막 n행 (최근 n일치)
# [흐름 이해] 사이드바의 days가 바뀌면 → 재실행 → 이 줄부터 아래가
# 전부 새 값으로 다시 계산됨. 필터와 본문을 연결하는 코드가 따로 없습니다!
view = df.tail(days)

# ============================================================
# [본문 상단] 제목 + KPI 카드 3장
# ============================================================
st.title("📈 그로스 대시보드")
st.caption(f"최근 {days}일 기준 | 데이터는 실습용 난수입니다")

# columns(3): 화면을 3등분. 반환된 c1, c2, c3가 각각의 "영역"입니다.
# [문법 팁] with c1: 블록 대신 c1.metric(...)처럼 점 표기도 됩니다.
#           내용이 한 줄이면 점 표기가 간결합니다.
with st.container(border=True):
    c1, c2, c3 = st.columns(3)

    # 증감(delta) 계산: (마지막날 / 첫날 - 1) * 100 = 기간 내 변화율(%)
    # [파이썬 문법] f"{값:+.1f}" → 부호를 항상 표시(+/-), 소수 1자리
    #   부호가 붙어야 metric이 초록▲/빨강▼을 판단할 수 있습니다.
    c1.metric("일평균 방문자", f"{view['방문자'].mean():,.0f}명",
              f"{(view['방문자'].iloc[-1] / view['방문자'].iloc[0] - 1) * 100:+.1f}%")
    c2.metric("일평균 가입자", f"{view['가입자'].mean():,.0f}명",
              f"{(view['가입자'].iloc[-1] / view['가입자'].iloc[0] - 1) * 100:+.1f}%")
    c3.metric("일평균 매출", f"{view['매출(만원)'].mean():,.0f}만원",
              f"{(view['매출(만원)'].iloc[-1] / view['매출(만원)'].iloc[0] - 1) * 100:+.1f}%")

# ============================================================
# [본문 중단] 탭: 같은 자리에 "차트 뷰"와 "데이터 뷰"를 겹쳐두기
# [왜 탭인가?] 차트와 원본 표를 위아래로 다 펼치면 화면이 길어집니다.
# 탭은 "같은 데이터의 다른 표현"을 한 자리에 겹쳐 화면을 아낍니다.
# ============================================================
tab_chart, tab_table = st.tabs(["📊 차트", "🗂️ 데이터"])

with tab_chart:
    if metrics:      # 지표를 1개 이상 선택했다면
        st.line_chart(view.set_index("날짜")[metrics])
    else:            # 아무것도 선택 안 하면 빈 차트 대신 친절한 안내
        st.info("사이드바에서 지표를 1개 이상 선택하세요")
        # ↑ [UX 원칙] 사용자의 조작으로 화면이 텅 비는 상황을 만들지 말 것.
        #   "왜 아무것도 없는지"를 항상 알려주는 것이 좋은 앱입니다.

with tab_table:
    st.dataframe(view, use_container_width=True)

# ============================================================
# [본문 하단] expander: 부가 정보는 접어두기
# [왜 접나?] 중요도가 낮은 정보를 항상 펼쳐두면 핵심이 묻힙니다.
# "필요한 사람만 클릭해서 본다"가 expander의 철학입니다.
# ============================================================
with st.expander("ℹ️ 지표 정의 보기"):
    st.markdown("""
    - **방문자**: 일별 고유 방문자 수 (DAU)
    - **가입자**: 일별 신규 가입 완료 수
    - **매출**: 일별 결제 금액 합계 (만원)
    """)

st.divider()
st.markdown("🎯 **미션**: `with st.container(border=True):`로 KPI 카드 3장 전체를 테두리 박스로 감싸보세요. (힌트: columns를 container 블록 안으로)")
